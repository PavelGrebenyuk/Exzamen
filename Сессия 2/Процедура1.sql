CREATE PROCEDURE Процедура1

		@IdАбонента int,
		@Номер_Абонента int, 
		@ФИО_Абонента nvarchar(255),
		@Адрес nvarchar(255)
		
AS
BEGIN
	insert into [dbo].[Абоненты]([IDАбонента],[Номер_Абонента],[ФИО_абонента],[Адрес])
Values
(
		@IdАбонента ,
		@Номер_Абонента , 
		@ФИО_Абонента ,
		@Адрес 
)
END
GO

